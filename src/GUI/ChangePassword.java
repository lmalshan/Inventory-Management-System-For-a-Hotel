    private void txtunKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtunKeyReleased
        // TODO add your handling code here:
        String ptt = "^[a-z0-9]+[@]{1}+[a-z]+[.]{1}+[a-z]{3}+$";
                        
        Pattern ptn = Pattern.compile(ptt);
        Matcher match = ptn.matcher(txtun.getText());
        if (!match.matches()) {
            ibiValidate.setText("Invalid username");
        }else{
            ibiValidate.setText("");
        }
    }//GEN-LAST:event_txtunKeyReleased

