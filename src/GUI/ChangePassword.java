    public void changePassword() throws ClassNotFoundException, SQLException {
        try {
            if ((!txtun.getText().isEmpty())
                    && (!txtnewpass.getText().isEmpty())
                    && (!txtconpass.getText().isEmpty())) {

                String username = txtun.getText();
                String newpassword = txtnewpass.getText();
                String confirmpassword = txtconpass.getText();

                if (txtnewpass.getText().equals(txtconpass.getText())) {
                    UpdatePasswordServices service1 = new UpdatePasswordServices();
                    service1.UpdatePassword(username, newpassword);

                    JOptionPane.showMessageDialog(rootPane, "Updated Successfully");
                } else {
                     JOptionPane.showMessageDialog(rootPane, "Password miss-matched!");
                }

            } else {
                throw new InvalidInputException();
            }
        } catch (InvalidInputException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", 0);
        }

    }

